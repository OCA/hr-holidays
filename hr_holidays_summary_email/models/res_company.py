from odoo import fields, models


class ResCompany(models.Model):
    _inherit = "res.company"

    hr_holidays_summary_weekly_dow = fields.Selection(
        selection=[
            ("0", "Monday"),
            ("1", "Tuesday"),
            ("2", "Wednesday"),
            ("3", "Thursday"),
            ("4", "Friday"),
            ("5", "Saturday"),
            ("6", "Sunday"),
        ],
        string="Leave Weekly Summary Day of Week",
        default="0",
        required=True,
    )
